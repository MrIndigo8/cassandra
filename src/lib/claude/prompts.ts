export function buildUserMessage(
  content: string,
  type?: string | null,
  direction?: string | null,
  timeframe?: string | null,
  quality?: string | null
): string {
  return `Текст: ${content}
Направленность: ${direction || 'не указано'}
Временное ощущение: ${timeframe || 'не указано'}
Качество: ${quality || 'не указано'}`;
}

